SELECT 
    D.DPT_ID AS [DEPARTMENT ID], 
    D.DPT_NAME AS [DEPARTMENT NAME],
    (
        SELECT COUNT(*) 
        FROM TBL_COURSEE C 
        WHERE C.DPT_ID = D.DPT_ID
    ) AS [NUMBER OF COURSES]
FROM TBL_DPT D;

SELECT D.DPT_ID AS [DEPARTMENT ID], D.DPT_NAME [DEPARTMNET NAME], COUNT(C.COURSE_ID) AS [NUMBER OF COURSES]
FROM TBL_DPT AS D
INNER JOIN
TBL_COURSEE AS C
ON
D.DPT_ID = C.DPT_ID
GROUP BY D.DPT_ID, D.DPT_NAME
HAVING COUNT(C.COURSE_ID) > 2;